package zad2;

public class Piwo {
    private String kolor;
    private String nazwa;


    public Piwo(String kolor, String nazwa) {
        this.kolor = kolor;
        this.nazwa = nazwa;
    }

    public String getKolor() {
        return kolor;
    }

    public String getNazwa() {
        return nazwa;
    }

}
